package io.github.backstreettoy.nullsafe.example.safecall;

/**
 * @author backstreettoy
 */
public class ObjectRelation {

    private MyObject upper;
    private MyObject bottom;
    private MyObject left;
    private MyObject right;

    public MyObject getUpper() {
        return upper;
    }

    public void setUpper(MyObject upper) {
        this.upper = upper;
    }

    public MyObject getBottom() {
        return bottom;
    }

    public void setBottom(MyObject bottom) {
        this.bottom = bottom;
    }

    public MyObject getLeft() {
        return left;
    }

    public void setLeft(MyObject left) {
        this.left = left;
    }

    public MyObject getRight() {
        return right;
    }

    public void setRight(MyObject right) {
        this.right = right;
    }
}
